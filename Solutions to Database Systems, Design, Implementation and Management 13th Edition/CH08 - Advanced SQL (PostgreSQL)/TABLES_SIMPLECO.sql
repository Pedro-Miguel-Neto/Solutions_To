CREATE TABLE CUSTOMER (
CUST_NUM NUMERIC(4,0) PRIMARY KEY,
CUST_LNAME VARCHAR(25) NOT NULL,
CUST_FNAME VARCHAR(25) NOT NULL,
CUST_BALANCE NUMERIC(15,2)
);

CREATE TABLE INVOICE (
INV_NUM NUMERIC(4,0) PRIMARY KEY,
CUST_NUM NUMERIC(4,0) REFERENCES CUSTOMER (CUST_NUM),
INV_DATE DATE NOT NULL,
INV_AMOUNT NUMERIC(15,2) NOT NULL
);

