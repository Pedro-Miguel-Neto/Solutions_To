CREATE TABLE JOB (
JOB_CODE NUMERIC(3,0) PRIMARY KEY,
JOB_DESCRIPTION VARCHAR(25) NOT NULL,
JOB_CHG_HOUR NUMERIC(7,2) NOT NULL,
JOB_LAST_UPDATE DATE NOT NULL
);

CREATE TABLE EMPLOYEE (
EMP_NUM NUMERIC(3,0) PRIMARY KEY,
EMP_LNAME VARCHAR(25) NOT NULL,
EMP_FNAME VARCHAR(25) NOT NULL,
EMP_INITIAL CHAR(1),
EMP_HIREDATE DATE NOT NULL,
JOB_CODE NUMERIC(3,0) REFERENCES JOB (JOB_CODE),
EMP_YEARS NUMERIC(3,0)
);

CREATE TABLE PROJECT (
PROJ_NUM NUMERIC(3,0) PRIMARY KEY,
PROJ_NAME VARCHAR(25) NOT NULL,
PROJ_VALUE NUMERIC(15,0),
PROJ_BALANCE NUMERIC(15,0),
EMP_NUM NUMERIC(3,0) REFERENCES EMPLOYEE (EMP_NUM)
);

CREATE TABLE ASSIGNMENT (
ASSIGN_NUM NUMERIC(4,0) PRIMARY KEY,
ASSIGN_DATE DATE,
PROJ_NUM NUMERIC(3,0) REFERENCES PROJECT (PROJ_NUM),
EMP_NUM NUMERIC(3,0) REFERENCES EMPLOYEE (EMP_NUM),
ASSIGN_JOB NUMERIC(3,0) NOT NULL,
ASSIGN_CHG_HR NUMERIC(7,2) NOT NULL,
ASSIGN_HOURS NUMERIC(5,1),
ASSIGN_CHARGE NUMERIC(7,2)
);

